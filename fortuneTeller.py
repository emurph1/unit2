num = int(input('Pick a number from 1-4: '))
if color == 'red' and num == 1:
    print('You will become a billionaire.')
if color == 'red' and num == 2:
    print('You will die today.')
if color == 'red' and num == 3:
    print('You will live forever')
if color == 'red' and num == 4:
    print('You will fail school')
if color == 'blue' and num == 1:
    print('You will lose everything.')
if color == 'blue' and num == 2:
    print('You will find love.')
if color == 'blue' and num == 3:
    print('You will be attacked by a bear.')
if color == 'blue' and num == 4:
    print('You will live a happy life.')